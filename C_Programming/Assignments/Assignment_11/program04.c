/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : RangeEvenSum
//  Description :   It is used to do addition of even numbers in specific range
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          31/10/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int RangeEvenSum(int iStart,int iEnd)
{
    int iCnt = 0;
    int iSum;

    for(iCnt = iStart; iCnt <= iEnd; iCnt++)
    {
        if((iCnt % 2) == 0){
            iSum = iSum + iCnt;
        }
    }

    return iSum;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
