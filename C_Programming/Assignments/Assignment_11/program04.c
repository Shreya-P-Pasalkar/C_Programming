/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1 = 0,iValue2 = 0;
    int iRet = 0;
    
    printf("Enter Starting Point:");
    scanf("%d",&iValue1);

    printf("Enter ending Point:");
    scanf("%d",&iValue2);

    iRet = RangeEvenSum(iValue1,iValue2);

    printf("Addition is %d",iRet);

    return 0;   
}

