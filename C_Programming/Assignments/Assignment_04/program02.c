#include<stdio.h>                                    // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FactRev
//  Description :   It is used to display factors in descending order
//  Input :         Integer
//  Output :        Void
//  Author :        Shreya Pramod Pasalkar
//  Date :          30/10/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

void FactRev(int iNo)
{
    int iCnt = 0;

    for(iCnt = (iNo/2); iCnt >= 1; iCnt--)
    {   
        if((iNo % iCnt) == 0)
        {
            printf("%d\t",iCnt);
        }    
    }
} 

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number:");
    scanf("%d",&iValue);

    FactRev(iValue);

    return 0;
}

