/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : NonFact
//  Description :   It is used to display nonfactors
//  Input :         Integer
//  Output :        Void
//  Author :        Shreya Pramod Pasalkar
//  Date :          30/10/2025
//
