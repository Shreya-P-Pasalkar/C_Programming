/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : print_numbers
//  Description :   It is used to print natural numbers
//  Input :         Integer
//  Output :        Void
//  Author :        Shreya Pramod Pasalkar
//  Date :          1/11/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

void print_numbers(int iNo)
{
    int iCnt = 0;

    if(iNo == 0)
    {
        printf("0 is not natural number");
    }

    if(iNo < 0)
    {
        iNo = -iNo;
    }


    for (iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("%d\t",iCnt);
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit;

    printf("Enter limit: ");
    scanf("%d",&limit);

    print_numbers(limit);

    return 0;
}

