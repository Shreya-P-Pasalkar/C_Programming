/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : print_EvenNumbers
//  Description :   It is used to print natural even numbers
//  Input :         Integer
//  Output :        Void
//  Author :        Shreya Pramod Pasalkar
//  Date :          1/11/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

void print_EvenNumbers(int iNo)
{
    int iCnt = 0;

    if(iNo == 0)
    {
        printf("0 is not natural number");
    }

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    for (iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if((iCnt % 2) == 0)
        {
            printf("%d\t",iCnt);
        }
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit;

    printf("Enter limit: ");
    scanf("%d",&limit);

    print_EvenNumbers(limit);

    return 0;
}

