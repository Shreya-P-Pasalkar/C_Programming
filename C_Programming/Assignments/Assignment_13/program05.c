/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SumNaturalEvenNumbers
//  Description :   It is used to do addition of natural even numbers
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          1/11/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int SumNaturalEvenNumbers(int iNo)
{
    int iCnt = 0;
    int iSum = 0;

    if(iNo == 0)
    {
        printf("0 is not natural number");
    }

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    for (iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if((iCnt % 2) == 0 )
        {
            iSum = iSum + iCnt;  
        } 
    }
    
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit;

    printf("Enter limit: ");
    scanf("%d",&limit);

    printf("%d\n",SumNaturalEvenNumbers(limit));

    return 0;
}

