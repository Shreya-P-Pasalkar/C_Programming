/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SumNaturalNumbers
//  Description :   It is used to do addition of natural numbers
//  Input :         Integers
//  Output :        Integers
//  Author :        Shreya Pramod Pasalkar
//  Date :          1/11/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int SumNaturalNumbers(int iNo)
{
    int iCnt = 0;
    int iSum = 0;

    if(iNo == 0)
    {
        printf("0 is not natural number");
    }

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    for (iCnt = 1; iCnt <= iNo; iCnt++)
    {
        iSum = iSum + iCnt;   
    }

    return iSum;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit;

    printf("Enter limit: ");
    scanf("%d",&limit);

    printf("%d\n",SumNaturalNumbers(limit));

    return 0;
}

