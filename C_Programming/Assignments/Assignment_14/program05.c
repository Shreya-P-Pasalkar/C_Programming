/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountFour
//  Description :   It is used to number of fours
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          1/11/2025
//
