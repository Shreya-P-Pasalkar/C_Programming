#include<stdio.h>                                    // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Sum_of_Factors
//  Description :   It is used to do addition of all factors of a number
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          1/11/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int Sum_of_Factors(int iNo)
{
    int iCnt = 0;
    int iSum= 0;

    for(iCnt = 1 ; iCnt <= (iNo/2); iCnt++)
    {
        if((iNo % iCnt) == 0)
        {
            iSum = iSum + iCnt;
        }
    }
    return iSum;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number;

    printf("Enter number : ");
    scanf("%d",&number);

    printf("%d\n", Sum_of_Factors(number));

    return 0;
}

