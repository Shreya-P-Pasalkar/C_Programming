#include<stdio.h>                                    // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Count_Factors
//  Description :   It is used to count factors of a number
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          1/11/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int Count_Factors(int iNo)
{
    int iCnt = 0;
    int iCount = 0;

    for(iCnt = 1 ; iCnt <= (iNo/2); iCnt++)
    {
        if((iNo % iCnt) == 0)
        {
            iCount = iCount + 1;
        }
    }
    return iCount;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number;

    printf("Enter number : ");
    scanf("%d",&number);

    printf("%d\n", Count_Factors(number));

    return 0;
}

