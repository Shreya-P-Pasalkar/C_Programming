#include<stdio.h>                                    // For Input Output
#include<stdbool.h>                                  // For Boolean Datatype

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Divisible_by_Five
//  Description :   It is used to check if number is divisible by 5 or not
//  Input :         Integer
//  Output :        Boolean
//  Author :        Shreya Pramod Pasalkar
//  Date :          1/11/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

bool Divisible_by_Five(int number)
{
    if((number % 5 ) == 0)
    {
        return true;
    }
    else
    {
        return false;
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number;

    printf("Enter Number :");
    scanf("%d",&number);

    printf("%s\n" ,Divisible_by_Five(number) ? "Yes" : "No");     // Ternary Operator

    return 0;
}

