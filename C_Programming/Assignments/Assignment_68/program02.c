#include<stdio.h>                                 // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Function Type : Recursive
//  Description :   It is used to do display specific pattern 
//  Input :         Nothing
//  Output :        void
//  Author :        Shreya Pramod Pasalkar
//  Date :          10/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////
void Display()
{
    static int i = 1;

    if(i <= 5)
    {
        printf("%d\t",i);
        i++;
        Display();
    }
    printf("\n");
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    Display();

    return 0;
}

