#include<stdio.h>                                 // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Sum
//  Function Type : Recursive
//  Description :   It is used to calculate sum of digits 
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          10/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int Sum(int No)
{
    static int iSum = 0;
    int iDigit = 0;

    if(No > 0)
    {
        iDigit = No % 10;
        iSum = iSum + iDigit;
        No = No/10;
        Sum(No);
    }
    return iSum;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int No = 0;
    int iRet = 0;

    printf("Enter Number : ");
    scanf("%d",&No);

    iRet = Sum(No);

    printf("Addition of digits is : %d\n",iRet);

    return 0;
}

