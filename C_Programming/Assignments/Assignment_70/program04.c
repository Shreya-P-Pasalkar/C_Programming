#include<stdio.h>                                 // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Factorial
//  Function Type : Recursive
//  Description :   It is used to calculate Factorial of a Number
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          10/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int Factorial(int No)
{
    static int iFact = 1;

    if(No >= 1)
    {
        iFact = iFact * No;
        No--;
        Factorial(No);
    }

    return iFact;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int No = 0;
    int iRet = 0;

    printf("Enter Number : ");
    scanf("%d",&No);

    iRet = Factorial(No);

    printf("Factorial of %d is : %d\n",No,iRet);

    return 0;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Input :    5
//  Output :   Factorial of 5 is : 120
