#include<stdio.h>                                 // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DigitProduct
//  Function Type : Recursive
//  Description :   It is used to calculate product of digits
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          10/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int DigitProduct(int No)
{
    static int iMult = 1;
    int iDigit = 0;

    if(No > 0)
    {
        iDigit = No % 10;
        iMult = iMult * iDigit;
        No = No/10;

        DigitProduct(No);
    }

    return iMult;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int No = 0;
    int iRet = 0;

    printf("Enter Number : ");
    scanf("%d",&No);

    iRet = DigitProduct(No);

    printf("Product of digits of %d is : %d\n",No,iRet);

    return 0;
}

