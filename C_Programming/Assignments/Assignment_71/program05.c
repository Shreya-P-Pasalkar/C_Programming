#include<stdio.h>                                 // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ReverseNumber
//  Function Type : Recursive
//  Description :   It is used to reverse the given number
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          10/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int ReverseNumber(int No)
{
    static int num = 0;
    int iDigit = 0;

    if(No > 0)
    {
        iDigit = No % 10;
        num = (num * 10) + iDigit;
        No = No/10;

        ReverseNumber(No);
    }

    return num;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int No = 0;
    int iRet = 0;

    printf("Enter Number : ");
    scanf("%d",&No);

    iRet = ReverseNumber(No);

    printf("Reverse Number is : %d\n",iRet);

    return 0;
}

