#include<stdio.h>                                 // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : MaxDigit
//  Function Type : Recursive
//  Description :   It is used to find the maximum digit of a number
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          10/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int MaxDigit(int No)
{
    static int iMax = 0;
    int iDigit = 0;

    if(No > 0)
    {
        iDigit = No % 10;
        if(iDigit > iMax)
        {
            iMax = iDigit;
        }
        No = No/10;

        MaxDigit(No);
    }

    return iMax;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int No = 0;
    int iRet = 0;

    printf("Enter Number : ");
    scanf("%d",&No);

    iRet = MaxDigit(No);

    printf("Maximum digit from %d is : %d\n",No,iRet);

    return 0;
}

