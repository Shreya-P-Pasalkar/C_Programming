#include<stdio.h>                                 // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : MinDigit
//  Function Type : Recursive
//  Description :   It is used to find the minimum digit of a number
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          10/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int MinDigit(int No)
{
    static int iMin = 9;
    int iDigit = 0;

    if(No > 0)
    {
        iDigit = No%10;
        if(iDigit < iMin)
        {
            iMin = iDigit;
        }
        No = No/10;

        MinDigit(No);
    }

    return iMin;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int No = 0;
    int iRet = 0;

    printf("Enter Number : ");
    scanf("%d",&No);

    iRet = MinDigit(No);

    printf("Minimum digit from %d is : %d\n",No,iRet);

    return 0;
}

