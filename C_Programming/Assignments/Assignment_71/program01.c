/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountSpaces
//  Function Type : Recursive
//  Description :   It is used to count the number of spaces in a string
//  Input :         String
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          10/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int CountSpaces(char * str)
{
    static int iSum = 0;
    static int i = 0;
    char ch = str[i];

    if(ch != '\0')
    {
        ch = str[i];
        if(ch == ' ')
        {
            iSum++;
        }
        i++;
        CountSpaces(str);
    }

    return iSum;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    char arr[20];
    int iRet = 0;

    printf("Enter String : ");
    scanf("%[^\n]s",arr);

    iRet = CountSpaces(arr);

    printf("Number of white spaces are : %d\n",iRet);

    return 0;
}

