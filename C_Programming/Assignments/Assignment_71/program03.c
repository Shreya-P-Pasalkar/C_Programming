#include<stdio.h>                                 // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountSmall
//  Function Type : Recursive
//  Description :   It is used to count the number of small letters in a string
//  Input :         String
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          10/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int CountSmall(char * str)
{
    static int iSum = 0;
    static int i = 0;
    char ch = str[i];

    if(ch != '\0')
    {
        ch = str[i];
        if((ch >= 'a') && (ch <= 'z'))
        {
            iSum++;
        }
        i++;
        CountSmall(str);
    }

    return iSum;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    char arr[20];
    int iRet = 0;

    printf("Enter String : ");
    scanf("%[^\n]s",arr);

    iRet = CountSmall(arr);

    printf("Number of small letters in a string is : %d\n",iRet);

    return 0;
}

