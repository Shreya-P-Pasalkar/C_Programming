#include<stdio.h>                                 // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Function Type : Recursive
//  Description :   It is used to do display specific pattern 
//  Input :         Integer
//  Output :        void
//  Author :        Shreya Pramod Pasalkar
//  Date :          10/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////
void Display(int No)
{
    static char ch = 'a';

    if(No >= 1)
    {
        printf("%c\t",ch);
        No--;
        ch++;
        Display(No);
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int No = 0;

    printf("Enter Number : ");
    scanf("%d",&No);

    Display(No);

    printf("\n");
    
    return 0;
}

