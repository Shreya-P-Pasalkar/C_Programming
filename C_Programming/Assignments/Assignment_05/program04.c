#include<stdio.h>                                    // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CheckNumberType
//  Description :   It is used to check the type of number i.e., Positive,Negative or zero
//  Input :         Integer
//  Output :        Void
//  Author :        Shreya Pramod Pasalkar
//  Date :          30/10/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

void CheckNumberType(int iNo)
{
    if(iNo < 0)
    {
        printf("%d is Negative",iNo);
    }
    else if(iNo > 0)
    {
        printf("%d is Positive",iNo);
    }
    else
    {
        printf("%d is Zero",iNo);
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iNum = 0;

    printf("Enter number:");
    scanf("%d",&iNum);

    CheckNumberType(iNum);

    return 0;
}

