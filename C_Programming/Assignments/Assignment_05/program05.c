/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FintLargest
//  Description :   It is used to find largest number among 3 numbers
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          30/10/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int FindLargest(int x,int y,int z)
{
    int iLarge = 0;
    if((x > y) && (x > z))
    {
        iLarge = x;
    }
    else if((y > z) && (y > x))
    {
        iLarge = y;
    }
    else
    {
        iLarge = z;
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iNum1,iNum2,iNum3,result;

    printf("Enter 3 Numbers:");
    scanf("%d %d %d",&iNum1,&iNum2,&iNum3);

    result = FindLargest(iNum1,iNum2,iNum3);

    printf("Largest number is %d\n",result);

    return 0;
}

