#include<stdio.h>                                    // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : MultDigits
//  Description :   It is used to number of digits between 3 and 7
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          1/11/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int MultDigits(int iNo)
{
    int iDigit = 0;
    int iMult = 1;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    while(iNo != 0)
    {
       iDigit = iNo % 10;
       
       if(iDigit == 0)
       {
            iMult = iMult * 1;
       }
       else
       {
            iMult = iMult * iDigit;
       }
       iNo = iNo / 10;
    }
    return iMult;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter number");
    scanf("%d",&iValue);

    iRet = MultDigits(iValue);

    printf("%d",iRet);

    return 0;
}

