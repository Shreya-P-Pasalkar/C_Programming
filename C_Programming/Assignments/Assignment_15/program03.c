#include<stdio.h>                                    // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountInbetween
//  Description :   It is used to number of digits between 3 and 7
//  Input :         Integer
//  Output :        Integer
//  Author :        Shreya Pramod Pasalkar
//  Date :          1/11/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int CountInbetween(int iNo)
{
    int iDigit = 0;
    int iCount = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    while(iNo != 0)
    {
       iDigit = iNo % 10;
       
       if((iDigit > 3) && (iDigit < 7))
       {
            iCount++;
       }
       iNo = iNo / 10;
    }
    return iCount;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter number");
    scanf("%d",&iValue);

    iRet = CountInbetween(iValue);

    printf("%d",iRet);

    return 0;
}

