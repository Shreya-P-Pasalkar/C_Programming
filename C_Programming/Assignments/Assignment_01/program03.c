#include<stdio.h>                // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   It is used to print 5 to 1 numbers
//  Input :         Integer
//  Output :        void
//  Author :        Shreya Pramod Pasalkar
//  Date :          30/10/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

void Display()
{
    int iCnt = 0;

    iCnt = 5;
    while(iCnt >= 1)
    {
        printf("%d\t",iCnt);
        iCnt--;
    }

}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;

}

