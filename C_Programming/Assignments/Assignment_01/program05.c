#include<stdio.h>                                           //For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Accept
//  Description :   It is used to print the accepted number of stars 
//  Input :         Integer
//  Output :        void
//  Author :        Shreya Pramod Pasalkar
//  Date :          30/10/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////

void Accept(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("*\t");
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    
    printf("Enter number of stars you want to display:");
    scanf("%d",&iValue);

    Accept(iValue);

    return 0 ;
}

