#include<stdio.h>                                    // For Input Output

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FhtoCs
//  Description :   It is used to convert fahrenheit to celsius
//  Input :         Float
//  Output :        Double
//  Author :        Shreya Pramod Pasalkar
//  Date :          31/10/2025
//
/////////////////////////////////////////////////////////////////////////////////////////////////


double FhtoCs(float fNo)
{
    double dCelsius = 0.0;

    dCelsius = ((fNo - 32)*(5.0/9.0));

    return dCelsius;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter temperature in Fahrenheit:");
    scanf("%f",&fValue);

    dRet = FhtoCs(fValue);

    printf("Celsius value is %lf",dRet);

    return 0;
}

