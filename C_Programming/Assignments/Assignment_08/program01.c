#include<stdio.h>                                    // For Input Output

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Number
//  Description :   It is used to display small,Medium and Large as it lies between 1 to 50,50 to 100 or greater than 100  
//  Input :         Integer
//  Output :        Void
//  Author :        Shreya Pramod Pasalkar
//  Date :          30/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


void Number(int iNo)
{
    if(iNo < 50)
    {
        printf("small");
    }
    else if((iNo > 50) && (iNo < 100) )
    {
        printf("medium");
    }
    else
    {
        printf("greater");
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iValue = 0;

    printf("Enter number:\n");
    scanf("%d",&iValue);

    Number(iValue);

    return 0;
}

