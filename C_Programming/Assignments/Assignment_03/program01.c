void PrintEven(int iNo)
{
    int iCnt = 0;

    if(iNo <= 0)
    {
        return ;
    }

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if(iCnt % 2 == 0)
        {
            printf("%d\t ", iCnt);
        }
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number:");
    scanf("%d",&iValue);

    PrintEven(iValue);

    return 0;
}

