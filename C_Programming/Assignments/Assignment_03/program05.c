#include<stdio.h>                                                                 // For Input Output
#include<stdbool.h>                                                               // For Boolean Datatype

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CheckVowel
//  Description :   It is used to check if character is vowel or not
//  Input :         Character
//  Output :        void
//  Author :        Shreya Pramod Pasalkar
//  Date :          30/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool CheckVowel(char chValue)
{
    if(chValue == 'A' || chValue == 'E' || chValue == 'I' || chValue == 'O' || chValue == 'U' ||
       chValue == 'a' || chValue == 'e' || chValue == 'i' || chValue == 'o' || chValue == 'u' )
    {
        return true;
    }
    else
    {
        return false;
    }

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function of the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';
    bool bRet = false;

    printf("Enter Character:");
    scanf("%c",&cValue);

    bRet = CheckVowel(cValue);

    if(bRet == true)
    {
        printf("It is Vowel");
    }
    else
    {
        printf("It is not a Vowel");
    }

    return 0;
}

